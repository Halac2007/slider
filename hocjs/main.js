function sumSuport(name) {
  const sumprice = contract
    .filter((c) =>
      c.Agencies.map((age) => age.name)
        .join()
        .toLocaleLowerCase()
        .includes(name.toLocaleLowerCase())
    )
    .map((c) => +c.price)
    .reduce((t, v) => t + v)

  const sumpaid = contract
    .filter((c) =>
      c.Agencies.map((age) => age.name)
        .join()
        .toLocaleLowerCase()
        .includes(name.toLocaleLowerCase())
    )
    .map((c) => +c.paid)
    .reduce((t, v) => t + v)

  const sumdept = contract
    .filter((c) =>
      c.Agencies.map((age) => age.name)
        .join()
        .toLocaleLowerCase()
        .includes(name.toLocaleLowerCase())
    )
    .map((c) => +c.debt_remain)
    .reduce((t, v) => t + v)

  return [sumprice, sumpaid, sumdept]
}

